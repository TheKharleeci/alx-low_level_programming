/**
 * _memset - fills memory with a constant byte
 *
 * @s: pointer to memory area to fill
 * @b: the constant byte
 * @n: the number of bytes to fill
 *
 * Return: char (a pointer to the memory area)
 */

char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i = 0;

	for ( ; i < n; i++)
	{
		s[i] = b;
	}

	return (s);
}
